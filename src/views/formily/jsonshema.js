// // 格式化后台行政下拉数据源
export const formatLocationOptions = (data = {}) => {
  const getKeyValueList = (obj = {}) => Object.entries(obj) || [];

  return getKeyValueList(data).map(([provinceCode, provinceObj]) => ({
    value: provinceCode,
    label: provinceObj.name,
    children: getKeyValueList(provinceObj.cities).map(([cityCode, cityObj]) => ({
      value: cityCode,
      label: cityObj.name,
      children: getKeyValueList(cityObj.districts).map(([code, name]) => ({
        value: code,
        label: name,
        children: null
      }))
    }))
  }));
};
